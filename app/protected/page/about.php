    <h2>THE STORY SO FAR</h2>
    <p>
        Growing up in the small village of <a href="https://goo.gl/maps/nEeU8gUW8WYwVteHA">Zuzgen</a>,
        Switzerland, SPARTALIEN (Marc Alexander Micoli) spent his early years balancing on a fulcrum
        between the visceral and virtual worlds — lurking in verdant forests surrounding his village,
        observing the alert and inquisitive nature of animals, building treehouses, and riding his bike,
        while also sending dot-bleep binary transmissions through coding and underground web forums.
        As a prolific producer, his music has always been informed by these polarities. Marc creates
        music for robot love, code for trees, and virtual survival manuals for three-dimensional beings
        through sound.
    </p>
    <p>
        Marc began producing music after discovering underground techno and punk parties in the late '90s.
        He started producing hip-hop, ambient, and trip-hop inspired <a href="#">sounds</a>
        as arT2 from 2001 until 2006.
        Eventually, his studio work developed into intricate trip-hop beats inspired by the lo-fi hip-hop of
        the '90s with found sound, dark ambient textures, cascading water, and atmospheric simulacra.
    </p>
    <p>
        He started <a href="#">DJing</a> in 2014 as lowtechman.
        In 2016, Marc purchased a Maschine Mikro and restored his vintage Rhodes Model 660 keyboard
        to finally produce his own sound again.
    </p>
    <p>
        Today, Marc focuses on producing music, carefully balancing tension and release with low-frequency
        synth washes matched with sampled vocals, restrained keys, and drum machines. His haunting and
        cinematic atmospherics create soundtracks for both solitude and community. Solitary keys pulse in
        syncopation, building slowly to rhythmic crescendo — only to disappear into the horizon between the
        visceral and the virtual, the known and the alien.
    </p>
